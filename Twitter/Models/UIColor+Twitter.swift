import UIKit

extension UIColor {
    static var twitterGray: UIColor {
        UIColor(named: "TwitterGray") ?? .systemGray
    }
    
    static var twitterRed: UIColor {
        UIColor(named: "TwitterRed") ?? .systemRed
    }
    
    static var twitterGreen: UIColor {
        UIColor(named: "TwitterGreen") ?? .systemGreen
    }
    
    static var twitterBlue: UIColor {
        UIColor(named: "TwitterBlue") ?? .systemBlue
    }
}
